public class Person{
    protected String name;
    protected String lastname;
    protected int  id;
    protected String tel;
    protected String email;



    public Person(String name , String lastname ,int id , String tel , String email ) {
        this.name = name;
        this.lastname = lastname;
        this.id = id;
        this.tel = tel;
        this.email = email;
    }
    
}
